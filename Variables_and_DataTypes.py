#Demonstrating Variables and Data Types in Python 

#String 
name = "Swaroop"  #String Value
print(f"String Example: name = {name} ")

#Integer
age = 21 #Integer Value
print(f"Integer Example: age = {age} ")

#Float 
Height = 5.5 #Float Value
print(f"Float Example: Height = {Height}")

#Boolean
is_active = True #Boolean Value
print(f"Boolean Example: is_active = {is_active}")

#String 
city = "Bengaluru"
print(f"String Example: city = {city}")

#Integer
roll_number = 101
print(f"Integer Example: roll_number = {roll_number}")

#Float
temperature = 36.6
print(f"Float Example: temperature = {temperature}")

#Boolean
is_student = False
print(f"Boolean Example: is_student = {is_student}")

#List (Ordered, can store multiple values)
fruits = ["apple", "banana", "cherry"]
print(f"List Example: fruits = {fruits}")

#Tuple (Similar to list but immutable - cannot be changed)
coordinates = (10.5, 20.3)
print(f"Tuple Example: coordinates = {coordinates}")

#Set (Unordered, unique values only)
unique_numbers = {1, 2, 3, 3, 4} 
print(f"Set Example (removes duplicates): unique_numbers = {unique_numbers}")

#Dictionary (Key-Value pairs)
student = {"name": "Swaroop", "age": 21, "course": "Python"}
print(f"Dictionary Example: student = {student}")

#None (Represents absence of value)
nothing = None
print(f"None Example: nothing = {nothing}")

#Complex Numbers (Real + Imaginary part)
complex_number = 3 + 4j
print(f"Complex Number Example: complex_number = {complex_number}")